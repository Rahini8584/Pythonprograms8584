# Write a program to perform all arithmetic operations on two numbers.
a=float(input("enter the first no."))
b=float(input("enter the second no."))
print(f"using + operator we get {a+b}")
print(f"using - operator we get {a-b}")
print(f"using * operator we get {a*b}")
print(f"using / operator we get {a/b}")
print(f"using % operator we get {a%b}")